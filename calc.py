def add(x: int, y: int) -> int:
     return x + y
 
def sub(x: int, y: int) -> int:
     return x - y
 
def prod(x: int, y: int) -> int:
    return x * y
 
def div(x: int, y: int) -> int:
    return x // y

if __name__ == "__main__":

    print(add(2, 2))
    print(sub(2, 2))
    print(prod(2, 2))
    print(div(2, 2))